    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        products.add(new Product(name.getText(),Double.parseDouble(price.getText()), Integer.parseInt(quantity.getText())));
        String data = "";
        for(int i = 0 ; i < products.size() ; i++)
        {
            Product p = products.get(i);
            data = data + (i+1)+" . "+p.getName()+"\t"+p.getQuantity()+"\t R"+p.getPrice()+"\n"+"\n";
            productNum.setText(p.numProducts(products)+" products");
            totalValue.setText("R"+new DecimalFormat("0.00").format(p.total(products)));
        }
        inventory.setText(data);
        
    }//GEN-LAST:event_jButton1ActionPerformed

