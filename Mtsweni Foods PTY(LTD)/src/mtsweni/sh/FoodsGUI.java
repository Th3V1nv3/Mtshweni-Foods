        selectedProducts.get(selectedProducts.size()-1).setQuantity(Integer.parseInt(numWanted.getText()));
       
        String data = "";
            for(int i = 0 ; i < selectedProducts.size() ; i++)
            {
                Product p = selectedProducts.get(i);
                data = data + (i+1)+" . "+p.getName()+"\t"+p.getQuantity()+"\t R"+p.getPrice()+"\n"+"\n";
                numBought.setText(p.numProducts(selectedProducts)+" products");
                totalCost.setText("R"+new DecimalFormat("0.00").format(p.total(selectedProducts)));
            }
            boughtItems.setText(data);
        
